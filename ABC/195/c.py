N = int(input())
if N < 1000:
    print(0)
elif N < 1000000:
    print(N - 1000 + 1)
elif N < 1000000000:
    print(999999 - 1000 + 1 + (N - 1000000 + 1) * 2)
elif N < 1000000000000:
    print(999999 - 1000 + 1 + (999999999 - 1000000 + 1) * 2 + (N - 1000000000 + 1) * 3)
elif N < 1000000000000000:
    print(
        999999
        - 1000
        + 1
        + (999999999 - 1000000 + 1) * 2
        + (999999999999 - 1000000000 + 1) * 3
        + (N - 1000000000000 + 1) * 4
    )
else:
    print(
        999999
        - 1000
        + 1
        + (999999999 - 1000000 + 1) * 2
        + (999999999999 - 1000000000 + 1) * 3
        + (999999999999999 - 1000000000000 + 1) * 4
        + 5
    )
